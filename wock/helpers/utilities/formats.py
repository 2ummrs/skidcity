async def convert(argument: str):
    percentage = re.compile(r"(?P<percentage>\d+)%")
    if argument.isdigit():
        argument = int(argument)
    elif match := percentage.match(argument):
        argument = int(match.group("percentage"))
    else:
        argument = 0

    if argument < 0 or argument > 100:
        raise commands.BadArgument("Percentage must be between 0 and 100")

    return argument